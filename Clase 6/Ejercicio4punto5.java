public class Ejercicio4punto5 {
    public static void main(String[] args) {
        int numero = 0;

        // Usando el operador %
        if (numero % 2 == 0) {
            System.out.println("El número " + numero + " es par.");
        } else {
            System.out.println("El número " + numero + " es impar.");
        }
    }
}

